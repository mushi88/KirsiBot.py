### Settings for bots, all need to be set for bot to work ###

Token="Put Token Here" ## Replace Put Token Here with your bots token.
Autorun_Music=False ## Change to True if you want music bot to launch on start.
User_ID="User ID" ## Put your User ID Here. Adds you to "Full" access.
Voice_Channel="Voice Channel ID" ## Replace Voice Channel ID with the id of the voice channel you want the bot to autojoin.
Server="Server ID" ## Replace Server ID with the id of the server that holds the channel your bot will autojoin.
Chat_Channel="Chat Channel ID" ## Replace Chat Channel ID with the text channel that the music bot will only accept commands from.
